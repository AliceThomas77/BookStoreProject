package BookShop;

public class Book {
	
	public String title;
	
	public String author;
	
	public String isbn;
	
	public String genre;	
	
	public Book(String title, String author, String isbn, String genre){
		this.title = title;
		this.author = author;
		this.isbn = isbn;
		this.genre = genre;
	}

}
